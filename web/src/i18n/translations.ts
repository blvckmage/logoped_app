export function t(lang: Language, key: string, params?: Record<string, string | number>): string {
  let text = translations[lang]?.[key] || translations['ru']?.[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}